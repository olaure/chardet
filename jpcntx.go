// JCANumCategories Number of categories
var JCANumCategories = 6

// JCADontKnow nope
var JCADontKnow = -1.0

// JCAEnoughRelThreshold enough
var JCAEnoughRelThreshold = 100

// JCAMaxRelThreshold max
var JCAMaxRelThreshold = 100

// JCAMinimumDataThreshold min
var JCAMinimumDataThreshold = 4

// JapaneseContextAnalysis (JP, KR) specific context analyzer
type JapaneseContextAnalysis struct {
	totalRel          int
	relSample         []int
	needToSkipCharNum int
	lastCharOrder     int
	done              bool
	getOrder          func([]byte, *string) (int, int)
	charSetName       *string
}

func (j *JapaneseContextAnalysis) reset() {
	j.totalRel = 0
	// category counters, each integer counts sequence in its category
	j.relSample = make([]int, JCANumCategories)
	// if last byte in current buffer is not the last byte of a character,
	// we need to know how many bytes to skip in next buffer
	j.needToSkipCharNum = 0
	j.lastCharOrder = -1 // The order of previous char
	// If this flag is set to True, detection is done and conclusion has
	// been made
	j.done = false
}

func (j *JapaneseContextAnalysis) gotEnoughData() bool {
	return j.totalRel > JCAEnoughRelThreshold
}

func (j *JapaneseContextAnalysis) getConfidence() float64 {
	// This is just one way to calculate confidence. It works well for me.
	if j.totalRel > JCAMinimumDataThreshold {
		return float64(j.totalRel-j.relSample[0]) / float64(j.totalRel)
	}
	return JCADontKnow
}

func (j *JapaneseContextAnalysis) feed(data []byte, numBytes int) {
	if j.done {
		return
	}
	// The buffer we got is byte oriented, and a character may span in more than one
	// buffers. In case the last one or two byte in last buffer is not
	// complete, we record how many byte needed to complete that character
	// and skip these bytes here.  We can choose to record those bytes as
	// well and analyse the character once it is complete, but since a
	// character will not make much difference, by simply skipping
	// this character will simply our logic and improve performance.
	skip := j.needToSkipCharNum
	for i := skip; i < numBytes; {
		boundary := i + 2
		if boundary > len(data) {
			boundary = len(data)
		}
		order, charLen := j.getOrder(data[i:boundary], j.charSetName)
		i += charLen
		if i > numBytes {
			j.needToSkipCharNum = i - numBytes
			j.lastCharOrder = -1
		} else {
			if order != -1 && j.lastCharOrder != -1 {
				j.totalRel++
				if j.totalRel > JCAMaxRelThreshold {
					j.done = true
					break
				}
				j.relSample[jp2CharContext[j.lastCharOrder][order]]++
			}
			j.lastCharOrder = order
		}
	}
}

