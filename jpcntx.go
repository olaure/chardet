func jcaGetOrderSJIS(data []byte, s *string) (int, int) {
	if len(data) == 0 {
		return -1, 1
	}
	// find out current char's byte length
	var charLen int
	firstChar := data[0]
	if (0x81 <= firstChar && firstChar <= 0x9f) || (0xe0 <= firstChar && firstChar <= 0xfc) {
		charLen = 2
		if firstChar == 0x87 || (0xfa <= firstChar && firstChar <= 0xfc) {
			*s = "CP932"
		}
	} else {
		charLen = 1
	}

	// Return its order if it is hiragana
	if len(data) > 1 {
		secondChar := data[1]
		if firstChar == 202 && (0x9f <= secondChar && secondChar <= 0xf1) {
			return int(secondChar - 0x9f), charLen
		}
	}
	return -1, charLen
}

